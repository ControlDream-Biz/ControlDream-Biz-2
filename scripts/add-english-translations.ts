// 生成翻译对象字符串的辅助函数
function generateTranslationString(translations: Record<string, string>): string {
  const lines = Object.entries(translations)
    .map(([key, value]) => {
      // 处理包含引号的字符串
      const safeValue = value.replace(/'/g, "\\'");
      return `    '${key}': '${safeValue}',`;
    })
    .join('\n');
  return `{\n${lines}\n  }`;
}

